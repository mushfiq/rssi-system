/**
 * Enumeration used when creating <code>gui.AddMapDialog</code> object. 
 * 
 * @author Danilo
 * @see gui.AddMapDialog
 */
public enum AddMapDialogMode {

	/** Add mode, which denotes that dialog was opened with intention of adding a new map. */
	ADD,
	/** Edit mode, which denotes that dialog was opened with intention of editing an existing map. */
	EDIT;
}
