/**
 * Enumeration for <code>gui.CoordinateZeroMarkerView</code>.
 * 
 * @author Danilo
 * @see gui.CoordinateZeroMarkerView
 */
public enum CoordinateZeroMarkerViewType {

	/**
	 * Lower left type, which denotes that this marker is the one that should be placed in the lower left corner of the
	 * map in the picture. This marker also denotes the zero coordinate point.
	 */
	LOWER_LEFT,
	/**
	 * Upper right type, which denotes that this marker is the one that should be placed in the upper right corner of
	 * the map in the picture.
	 */
	UPPER_RIGHT;
}
